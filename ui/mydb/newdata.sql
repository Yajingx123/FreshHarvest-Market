TRUNCATE PurchaseItem;

INSERT INTO PurchaseItem(purchase_order_ID, item_ID, product_ID, unit_cost, received_date)
SELECT
  s.purchase_order_ID,
  s.item_ID,
  s.product_ID,
  i.unit_cost,
  s.received_date
FROM StockItem s
JOIN Inventory i
  ON i.batch_ID = s.batch_ID
WHERE s.purchase_order_ID IS NOT NULL;